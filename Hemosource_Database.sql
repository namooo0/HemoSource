	Apos INT NOT NULL,
	Aneg INT NOT NULL,
	Bpos INT NOT NULL,
	Bneg INT NOT NULL,
	ABpos INT NOT NULL,
	ABneg INT NOT NULL,
	PRIMARY KEY(Name),
	FOREIGN KEY(Name) REFERENCES Banks_Hospitals(Name)
